package com.example.javachipnavigationbar.Community_ui;

import java.util.Date;

public class Post extends com.example.javachipnavigationbar.Community_ui.PostId {
    private String image , user , caption;
    private Date time;

    public String getImage() {
        return image;
    }

    public String getUser() {
        return user;
    }

    public String getCaption() {
        return caption;
    }

    public Date getTime() {
        return time;
    }
}
